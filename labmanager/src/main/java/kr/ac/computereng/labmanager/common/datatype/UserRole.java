package kr.ac.computereng.labmanager.common.datatype;

public class UserRole {
	private String id;
	private Role role;
	
	UserRole(){
		this.role = Role.YET;
	};
	
	public UserRole(String id) {
		this.id = id;
		this.role = Role.YET;
	}
	
	public UserRole(String id,Role role) {
		this.id = id;
		this.role = role;
	}

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Role getRole() {
		return role;
	}
	public void setRole(Role role) {
		this.role = role;
	}
}
